#!/usr/bin/env node
// Build a single concatenated "context pack" from the knowledge files, for
// one-shot ingestion by an LLM (system prompt, fine-tune context, or a single
// fetchable URL). No dependencies.
//
//   node ai/scripts/build-context-pack.mjs
//
// Output:
//   aiqo-web/public/ai/context.md   (served at https://aiqo.app/ai/context.md)

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..", "..");
const KNOWLEDGE = path.join(ROOT, "ai", "knowledge");

// Curated order: overview first, then persona, features, flows, glossary, FAQ.
const ORDER = [
  "AIQO_KNOWLEDGE_BASE.md",
  "AIQO_PRODUCT_VISION.md",
  "CAPTAIN_HAMMOUDI_PROFILE.md",
  "AIQO_FEATURES.md",
  "AIQO_USER_FLOWS.md",
  "AIQO_GLOSSARY.md",
  "AIQO_FAQ.md",
];

const header = `# AiQo — Combined Knowledge Context Pack

This file concatenates the AiQo knowledge base for one-shot ingestion by an AI
system. It is generated by ai/scripts/build-context-pack.mjs — do not edit by
hand; edit the source files in ai/knowledge/ and re-run the script.

Tiers: Free < AiQo Max ($9.99/mo) < AiQo Intelligence Pro ($19.99/mo), plus a
7-day trial. AiQo is not a medical device. Tribe is built but not yet live.

---

`;

let body = header;
let count = 0;
for (const name of ORDER) {
  const fp = path.join(KNOWLEDGE, name);
  if (!fs.existsSync(fp)) { console.warn(`! skipped missing ${name}`); continue; }
  body += `\n\n<!-- ===== source: ai/knowledge/${name} ===== -->\n\n`;
  body += fs.readFileSync(fp, "utf8").trim() + "\n";
  count++;
}

const out = path.join(ROOT, "aiqo-web", "public", "ai", "context.md");
fs.writeFileSync(out, body);
console.log(`✓ wrote ${path.relative(ROOT, out)} from ${count} knowledge files (${body.length} chars)`);
